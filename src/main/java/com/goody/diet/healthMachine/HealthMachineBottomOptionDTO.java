package com.goody.diet.healthMachine;

public class HealthMachineBottomOptionDTO {

	private Long bottomOptionNum;
	private Long topOptionNum;
	private String optionName;
	
	
	
	public Long getBottomOptionNum() {
		return bottomOptionNum;
	}
	public void setBottomOptionNum(Long bottomOptionNum) {
		this.bottomOptionNum = bottomOptionNum;
	}
	public Long getTopOptionNum() {
		return topOptionNum;
	}
	public void setTopOptionNum(Long topOptionNum) {
		this.topOptionNum = topOptionNum;
	}
	public String getOptionName() {
		return optionName;
	}
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	
	
}
